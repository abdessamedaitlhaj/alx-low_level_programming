/**
 * op_add - adds two numbers.
 * @a: first num.
 * @b: second num.
 *
 * Return: the sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - the substruction of two numbers.
 * @a: first num.
 * @b: second num.
 *
 * Return: the result of substruction of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication of two numbers.
 * @a: first num.
 * @b: second num.
 *
 * Return: the multiplication of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: the division of a and b.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - the modulo of two numbers.
 * @a: first number.
 * @b: second number.
 *
 * Return: the result of modulo of a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
